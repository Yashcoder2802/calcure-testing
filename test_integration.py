"""
Integration Tests — Calcure Calendar & Task Manager
====================================================
Integration tests verify that multiple modules work TOGETHER correctly.
Each test exercises a realistic pipeline: load → transform → filter → save.

Unlike unit tests (single class in isolation), these tests cross module
boundaries and validate end-to-end data flow.
"""

import sys
import os
import time
import tempfile
import unittest

sys.path.insert(0, os.path.join(os.path.dirname(__file__)))

from calcure.data import (
    Task, UserEvent, Event, Timer, Tasks, Events, Birthdays,
    Status, Frequency, RepeatedEvents
)
from calcure.calendars import Calendar


# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------

def make_timer(stamps=None):
    return Timer(stamps or [])


def make_task(item_id=0, name="Task", status=Status.NORMAL,
              privacy=False, year=0, month=0, day=0):
    return Task(item_id, name, status, make_timer(), privacy, year, month, day)


def make_event(item_id=0, year=2024, month=3, day=1, name="Event",
               repetition=1, frequency=Frequency.ONCE,
               status=Status.NORMAL, privacy=False):
    return UserEvent(item_id, year, month, day, name, repetition,
                     frequency, status, privacy)


def make_config(tasks_file, events_file):
    class FakeCF:
        TASKS_FILE = tasks_file
        EVENTS_FILE = events_file
        USE_PERSIAN_CALENDAR = False
    return FakeCF()


def make_ics_config(task_files=None, event_files=None):
    class FakeCF:
        ICS_TASK_FILES = task_files
        ICS_EVENT_FILES = event_files
        USE_PERSIAN_CALENDAR = False
    return FakeCF()


class MockScreen:
    def __init__(self, year, month, day):
        self.year = year
        self.month = month
        self.day = day


