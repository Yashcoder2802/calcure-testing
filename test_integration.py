class TestTasksTimerIntegration(unittest.TestCase):
    """
    Integration: Tasks collection + Timer lifecycle.
    Validates that timer state changes propagate correctly through the Tasks API.
    """

    def test_start_pause_resume_reset_cycle(self):
        """
        Full timer lifecycle on a single task:
        add → start → pause → resume → reset → verify clean state.
        """
        tasks = Tasks()
        tasks.add_item(make_task(item_id=0))

        # Start
        tasks.add_timestamp_for_task(0)
        self.assertTrue(tasks.has_active_timer)
        self.assertTrue(tasks.items[0].timer.is_counting)

        # Pause
        tasks.add_timestamp_for_task(0)
        self.assertFalse(tasks.items[0].timer.is_counting)
        self.assertFalse(tasks.has_active_timer)

        # Resume
        tasks.add_timestamp_for_task(0)
        self.assertTrue(tasks.items[0].timer.is_counting)

        # Reset
        tasks.reset_timer_for_task(0)
        self.assertFalse(tasks.items[0].timer.is_started)
        self.assertFalse(tasks.has_active_timer)

    def test_only_one_timer_active_after_pause_others(self):
        """
        Start 3 task timers, then call pause_all_other_timers(1).
        Only task 1 should remain running; tasks 0 and 2 are paused.
        """
        tasks = Tasks()
        for i in range(3):
            tasks.add_item(make_task(item_id=i))
            tasks.add_timestamp_for_task(i)

        tasks.pause_all_other_timers(1)

        self.assertFalse(tasks.items[0].timer.is_counting)
        self.assertTrue(tasks.items[1].timer.is_counting)
        self.assertFalse(tasks.items[2].timer.is_counting)
        self.assertTrue(tasks.has_active_timer)

    def test_timer_elapsed_time_persists_across_collection_rename(self):
        """
        Renaming a task via the collection API does not affect its timer state.
        """
        tasks = Tasks()
        tasks.add_item(make_task(item_id=0, name="Old name"))
        tasks.add_timestamp_for_task(0)
        tasks.rename_item(0, "New name")

        self.assertEqual(tasks.items[0].name, "New name")
        self.assertTrue(tasks.items[0].timer.is_counting)


