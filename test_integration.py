class TestCalendarFilterIntegration(unittest.TestCase):
    """
    Integration: Calendar (calendars.py) + Collection.filter_events_that_month.
    Validates that calendar grid dimensions align with event filtering results.
    """

    def test_week_count_matches_grid_rows(self):
        """
        For every month of 2024, the number of week numbers returned by
        month_week_numbers() equals the number of grid rows from monthdayscalendar().
        """
        cal = Calendar(0, False)
        for month in range(1, 13):
            rows = cal.monthdayscalendar(2024, month)
            wnums = cal.month_week_numbers(2024, month)
            self.assertEqual(len(rows), len(wnums),
                             f"Mismatch for month {month}")

    def test_filter_returns_subset_of_full_month_grid(self):
        """
        Events filtered for a specific day must be a strict subset of all
        events that appear in that month's filter.
        """
        events = Events()
        for day in [1, 5, 10, 15, 20, 25]:
            events.add_item(make_event(item_id=day, year=2024, month=5, day=day,
                                       name=f"Day {day} event"))

        month_screen = MockScreen(2024, 5, 1)
        day_screen = MockScreen(2024, 5, 10)

        month_result = events.filter_events_that_month(month_screen)
        day_result = events.filter_events_that_day(day_screen)

        self.assertEqual(len(month_result.items), 6)
        self.assertEqual(len(day_result.items), 1)
        self.assertIn(day_result.items[0].name,
                      {e.name for e in month_result.items})

    def test_all_31_days_of_march_are_reachable_via_calendar(self):
        """
        monthdayscalendar for March 2024 must contain all 31 days,
        all of which can be used as filter screen.day values.
        """
        cal = Calendar(0, False)
        events = Events()
        for day in range(1, 32):
            events.add_item(make_event(item_id=day, year=2024, month=3, day=day,
                                       name=f"Event {day}"))

        all_days_in_grid = {
            day
            for week in cal.monthdayscalendar(2024, 3)
            for day in week
            if day != 0
        }
        self.assertEqual(len(all_days_in_grid), 31)

        for day in all_days_in_grid:
            screen = MockScreen(2024, 3, day)
            result = events.filter_events_that_day(screen)
            self.assertEqual(len(result.items), 1)


