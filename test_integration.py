class TestICSImportPipelines(unittest.TestCase):
    """
    Integration: LoaderICS subclasses + domain objects.
    These tests execute real .ics files through the import pipeline and then
    validate the resulting Tasks/Events collections.
    """

    def test_vtodo_file_import_keeps_completed_task_and_skips_cancelled(self):
        """
        TaskLoaderICS should import a COMPLETED VTODO as DONE, preserve its due
        date, and ignore a CANCELLED VTODO from the same calendar file.
        """
        from calcure.loaders import TaskLoaderICS

        ics_text = """BEGIN:VCALENDAR
VERSION:2.0
PRODID:-//Calcure Test//EN
BEGIN:VTODO
SUMMARY:Done item
STATUS:COMPLETED
PRIORITY:1
DUE;VALUE=DATE:20240415
END:VTODO
BEGIN:VTODO
SUMMARY:Cancelled item
STATUS:CANCELLED
DUE;VALUE=DATE:20240416
END:VTODO
END:VCALENDAR
"""

        with tempfile.NamedTemporaryFile(suffix=".ics", delete=False, mode="w",
                                         encoding="utf-8") as f:
            f.write(ics_text)
            filepath = f.name

        try:
            loaded = TaskLoaderICS(make_ics_config(task_files=[filepath])).load()
            self.assertEqual(len(loaded.items), 1)
            task = loaded.items[0]
            self.assertEqual(task.name, "Done item")
            self.assertEqual(task.status, Status.DONE)
            self.assertEqual((task.year, task.month, task.day), (2024, 4, 15))
        finally:
            os.unlink(filepath)

    def test_all_day_multiday_ics_event_loads_as_daily_repetition(self):
        """
        EventLoaderICS should turn an all-day multi-day VEVENT into a DAILY
        event with repetition equal to the number of included calendar days.
        """
        from calcure.loaders import EventLoaderICS

        ics_text = """BEGIN:VCALENDAR
VERSION:2.0
PRODID:-//Calcure Test//EN
BEGIN:VEVENT
SUMMARY:Festival
DTSTART;VALUE=DATE:20240301
DTEND;VALUE=DATE:20240304
END:VEVENT
END:VCALENDAR
"""

        with tempfile.NamedTemporaryFile(suffix=".ics", delete=False, mode="w",
                                         encoding="utf-8") as f:
            f.write(ics_text)
            filepath = f.name

        try:
            loaded = EventLoaderICS(make_ics_config(event_files=[filepath])).load()
            self.assertEqual(len(loaded.items), 1)
            event = loaded.items[0]
            self.assertEqual(event.frequency, Frequency.DAILY)
            self.assertEqual(event.repetition, 3)
        finally:
            os.unlink(filepath)

    def test_rrule_event_with_exdate_filters_only_remaining_occurrence(self):
        """
        An RRULE event imported from .ics should flow through EventLoaderICS into
        RepeatedEvents, and EXDATE must suppress the excluded repeated date.
        """
        from calcure.loaders import EventLoaderICS

        ics_text = """BEGIN:VCALENDAR
VERSION:2.0
PRODID:-//Calcure Test//EN
BEGIN:VEVENT
SUMMARY:Recurring
DTSTART;VALUE=DATE:20240301
DTEND;VALUE=DATE:20240302
RRULE:FREQ=WEEKLY;COUNT=3
EXDATE;VALUE=DATE:20240308
END:VEVENT
END:VCALENDAR
"""

        with tempfile.NamedTemporaryFile(suffix=".ics", delete=False, mode="w",
                                         encoding="utf-8") as f:
            f.write(ics_text)
            filepath = f.name

        try:
            loaded = EventLoaderICS(make_ics_config(event_files=[filepath])).load()
            repeated = RepeatedEvents(loaded, False, 2024)

            excluded = repeated.filter_events_that_day(MockScreen(2024, 3, 8))
            remaining = repeated.filter_events_that_day(MockScreen(2024, 3, 15))

            self.assertEqual(len(excluded.items), 0)
            self.assertEqual(len(remaining.items), 1)
            self.assertEqual(remaining.items[0].name, "Recurring")
        finally:
            os.unlink(filepath)


