class TestTaskSaveLoadModifyCycle(unittest.TestCase):
    """
    Integration: data.Tasks → savers.TaskSaverCSV → loaders.TaskLoaderCSV → modify → save again.
    Validates that the full persistence cycle preserves state correctly.
    """

    def setUp(self):
        self.tmpfile = tempfile.NamedTemporaryFile(suffix=".csv", delete=False, mode="w")
        self.tmpfile.close()
        self.filepath = self.tmpfile.name
        self.cf = make_config(self.filepath, self.filepath)

    def tearDown(self):
        for path in [self.filepath, self.filepath + ".bak"]:
            if os.path.exists(path):
                os.unlink(path)

    def test_save_then_load_preserves_all_fields(self):
        """
        Save 3 tasks (with different statuses and privacy) then reload.
        All field values must be identical.
        """
        from calcure.savers import TaskSaverCSV
        from calcure.loaders import TaskLoaderCSV

        tasks = Tasks()
        tasks.add_item(Task(0, "Normal public",    Status.NORMAL,      make_timer(), False, 0, 0, 0))
        tasks.add_item(Task(1, "Done private",     Status.DONE,        make_timer(), True,  2025, 3, 15))
        tasks.add_item(Task(2, "Important public", Status.IMPORTANT,   make_timer(), False, 0, 0, 0))
        TaskSaverCSV(tasks, self.cf).save()

        loaded = TaskLoaderCSV(self.cf).load()
        self.assertEqual(len(loaded.items), 3)

        names = {t.name: t for t in loaded.items}
        self.assertEqual(names["Normal public"].status,    Status.NORMAL)
        self.assertEqual(names["Done private"].status,     Status.DONE)
        self.assertEqual(names["Done private"].privacy,    True)
        self.assertEqual(names["Done private"].year,       2025)
        self.assertEqual(names["Important public"].status, Status.IMPORTANT)

    def test_modify_after_load_and_resave(self):
        """
        Load tasks, modify a status, save again, reload.
        The modification must survive the second save/load cycle.
        """
        from calcure.savers import TaskSaverCSV
        from calcure.loaders import TaskLoaderCSV

        tasks = Tasks()
        tasks.add_item(Task(0, "Task to modify", Status.NORMAL, make_timer(), False, 0, 0, 0))
        TaskSaverCSV(tasks, self.cf).save()

        loaded = TaskLoaderCSV(self.cf).load()
        loaded.toggle_item_status(0, Status.DONE)
        TaskSaverCSV(loaded, self.cf).save()

        reloaded = TaskLoaderCSV(self.cf).load()
        self.assertEqual(reloaded.items[0].status, Status.DONE)

    def test_delete_and_resave(self):
        """
        Add 3 tasks, save, load, delete one, save again, reload.
        Only 2 tasks should remain.
        """
        from calcure.savers import TaskSaverCSV
        from calcure.loaders import TaskLoaderCSV

        tasks = Tasks()
        for i, name in enumerate(["Alpha", "Beta", "Gamma"]):
            tasks.add_item(Task(i, name, Status.NORMAL, make_timer(), False, 0, 0, 0))
        TaskSaverCSV(tasks, self.cf).save()

        loaded = TaskLoaderCSV(self.cf).load()
        loaded.delete_item(1)
        TaskSaverCSV(loaded, self.cf).save()

        reloaded = TaskLoaderCSV(self.cf).load()
        self.assertEqual(len(reloaded.items), 2)
        names = {t.name for t in reloaded.items}
        self.assertNotIn("Beta", names)

    def test_changed_flag_reset_after_save(self):
        """
        After TaskSaverCSV.save(), the changed flag on the Tasks collection
        is reset to False.
        """
        from calcure.savers import TaskSaverCSV

        tasks = Tasks()
        tasks.add_item(make_task())
        self.assertTrue(tasks.changed)
        TaskSaverCSV(tasks, self.cf).save()
        self.assertFalse(tasks.changed)


