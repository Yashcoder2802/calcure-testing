class TestEventsRepeatedEventsFilterPipeline(unittest.TestCase):
    """
    Integration: data.Events → data.RepeatedEvents → Collection.filter_events_that_day.
    Validates that recurring events generated by RepeatedEvents are properly
    discoverable through the filter API.
    """

    def test_weekly_event_visible_on_repeated_date(self):
        """
        Create a weekly event starting 2024-03-01. RepeatedEvents generates
        occurrences on March 8 and 15. Filtering March 8 must return that event.
        """
        user_events = Events()
        user_events.add_item(make_event(
            item_id=0, year=2024, month=3, day=1,
            name="Weekly standup", repetition=3, frequency=Frequency.WEEKLY
        ))
        repeated = RepeatedEvents(user_events, False, 2024)

        screen = MockScreen(2024, 3, 8)
        result = repeated.filter_events_that_day(screen)
        self.assertEqual(len(result.items), 1)
        self.assertEqual(result.items[0].name, "Weekly standup")

    def test_original_event_not_in_repeated_collection(self):
        """
        The original event (March 1) must NOT appear in the RepeatedEvents
        collection — it remains only in user_events.
        """
        user_events = Events()
        user_events.add_item(make_event(
            item_id=0, year=2024, month=3, day=1,
            name="Original", repetition=3, frequency=Frequency.WEEKLY
        ))
        repeated = RepeatedEvents(user_events, False, 2024)

        screen = MockScreen(2024, 3, 1)
        result = repeated.filter_events_that_day(screen)
        self.assertEqual(len(result.items), 0)

    def test_original_event_visible_in_user_events(self):
        """
        Conversely, the original date IS visible when querying user_events directly.
        """
        user_events = Events()
        user_events.add_item(make_event(
            item_id=0, year=2024, month=3, day=1,
            name="Original", repetition=3, frequency=Frequency.WEEKLY
        ))
        screen = MockScreen(2024, 3, 1)
        result = user_events.filter_events_that_day(screen)
        self.assertEqual(len(result.items), 1)

    def test_monthly_event_filter_across_months(self):
        """
        A monthly event on 2024-01-15 with repetition=3 produces occurrences
        on Feb 15 and Mar 15. All three months can be queried independently.
        """
        user_events = Events()
        user_events.add_item(make_event(
            item_id=0, year=2024, month=1, day=15,
            name="Monthly review", repetition=3, frequency=Frequency.MONTHLY
        ))
        repeated = RepeatedEvents(user_events, False, 2024)

        for month, expected_count in [(1, 0), (2, 1), (3, 1)]:
            screen = MockScreen(2024, month, 15)
            result = repeated.filter_events_that_day(screen)
            self.assertEqual(len(result.items), expected_count,
                             f"Wrong count for month {month}")

    def test_combined_original_and_repeated_total(self):
        """
        On a day that has both an original event AND a repeated event from a
        different original, the combined queries return the right totals.
        """
        user_events = Events()
        user_events.add_item(make_event(
            item_id=0, year=2024, month=4, day=10,
            name="Original on 10th", repetition=1, frequency=Frequency.ONCE
        ))
        user_events.add_item(make_event(
            item_id=1, year=2024, month=4, day=3,
            name="Weekly from 3rd", repetition=2, frequency=Frequency.WEEKLY
        ))
        repeated = RepeatedEvents(user_events, False, 2024)

        screen = MockScreen(2024, 4, 10)
        from_original = user_events.filter_events_that_day(screen)
        from_repeated = repeated.filter_events_that_day(screen)
        total = len(from_original.items) + len(from_repeated.items)
        self.assertEqual(total, 2)


